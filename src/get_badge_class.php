<?php
    //Συνάρτηση που χρησιμοποιείται για να εμφανίσει σε κάθε card του issue στην browse σελίδα το απαραίτητο badge ανάλογα με την καταχωρημένη κατάσταση του issue στην db
    
function get_badge_class(string $status): string {
    // Χρησιμοποιούμε switch για να ελέγξουμε τις διαφορετικές καταστάσεις
    switch ($status) {
        case 'Υποβλήθηκε':
            return 'submitted'; // Γαλάζιο χρώμα για νέες αναφορές
            
        case 'Σε εξέλιξη': // Προσθέτουμε και την πεζή γραφή για σιγουριά
            return 'running'; // Κίτρινο χρώμα 
            
        case 'Επιλύθηκε':
            return 'resolved'; // Πράσινο χρώμα για τα ολοκληρωμένα
            
        default:
            return 'default'; // Γκρι χρώμα αν υπάρξει κάποια άγνωστη κατάσταση (π.χ. κενό)
    }
}

?>
